#ifndef ITER_HPP
# define ITER_HPP

/* @------------------------------------------------------------------------@ */
/* |                            Include Section                             | */
/* @------------------------------------------------------------------------@ */

#include <cstddef> // For std::size_t

/* @------------------------------------------------------------------------@ */
/* |                            Template Section                            | */
/* @------------------------------------------------------------------------@ */

template <typename T, typename Func>
void iter(T* array, std::size_t length, Func& f)
{
	for (std::size_t i = 0; i < length; ++i)
	{
		f(array[i]);
	}
}

#endif
